public class Item {
    private String nome;
    private int quantidade;
    private double supurte;

    public String getNome() {
        return nome;
    }

    public int getQuantidade() {
        return quantidade;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setQuantidade(int quantidade) {
        this.quantidade = quantidade;
    }

    public double getSupurte() {
        return supurte;
    }

    public void setSupurte(double supurte) {
        this.supurte = supurte;
    }
    
}
